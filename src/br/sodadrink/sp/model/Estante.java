package br.sodadrink.sp.model;

public class Estante {

	int id_estante;
	String nome;

	public int getId_estante() {
		return id_estante;
	}

	public void setId_estante(int id_estante) {
		this.id_estante = id_estante;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

}
