package br.sodadrink.sp.model;

public class Corredor {

	int id_corredor;
	String nome;

	public int getId_corredor() {
		return id_corredor;
	}

	public void setId_corredor(int id_corredor) {
		this.id_corredor = id_corredor;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

}
